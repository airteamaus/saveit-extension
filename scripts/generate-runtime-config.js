#!/usr/bin/env node

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const outputPath = path.join(__dirname, '..', 'src', 'runtime-config.generated.js');
const unsplashAccessKey = process.env.SAVEIT_UNSPLASH_ACCESS_KEY || '';
const escapedUnsplashAccessKey = unsplashAccessKey
  .replace(/\\/g, '\\\\')
  .replace(/'/g, '\\\'');

const content = `// Generated by scripts/generate-runtime-config.js
// Safe default: no Unsplash key unless explicitly provided at build time.
export const unsplashAccessKey = '${escapedUnsplashAccessKey}';
`;

fs.writeFileSync(outputPath, content);
console.log(`Generated runtime config at ${outputPath}`);
